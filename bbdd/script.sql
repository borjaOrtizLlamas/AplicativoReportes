CREATE TABLE APLICACIONES (
		ID NUMBER(38 , 0) NOT NULL,
		NOMBRE_APLICACION VARCHAR2(100),
		FUNCIONAL VARCHAR2(200),
		WINDOWS NUMBER(1 , 0),
		EMAIL_FUNCIONAL VARCHAR2(200)
	);

	
	CREATE TABLE USUARIOS_MEDAS (
		USUARIO VARCHAR2(100) NOT NULL,
		CONTRASENA VARCHAR2(100),
		VALIDO NUMBER(1 , 0),
		NOMBRE VARCHAR2(200),
		TELEFONO NUMBER(38 , 0),
		EXTENSION NUMBER(38 , 0),
		MOVIL NUMBER(38 , 0),
		EMAIL VARCHAR2(200)
	);

CREATE TABLE INCIDENCIAS (
		ID NUMBER NOT NULL,
		USUARIO VARCHAR2(100),
		ENTORNO VARCHAR2(100),
		BBDD NUMBER(1 , 0),
		APLICATIVO NUMBER(1 , 0),
		NOMBRE_APLICACION VARCHAR2(100),
		FECHA VARCHAR2(200),
		DESCRIPCCION VARCHAR2(4000),
		DOCUMENTACION VARCHAR2(4000),
		MOTIVO VARCHAR2(4000),
		REQUIEREPARADA1 VARCHAR2(50),
		DESCRIPCION_DOC VARCHAR2(4000),
		RELASE_NOTES VARCHAR2(4000),
		AUTORIZADOR VARCHAR2(4000),
		CORREO_AUTORIZADOR VARCHAR2(4000),
		REVISADA NUMBER(1 , 0),
		FECHA_CREACION_INCIDENCIA VARCHAR2(80),
		ASUNTO VARCHAR2(200),
		URGENTE NUMBER(1 , 0),
		CODIGO_UNICENTER VARCHAR2(100),
		CORREOS_ENVIO VARCHAR2(300),
		CORREOS_COPIA VARCHAR2(300),
		ESTADO NUMBER,
		AUTORIZACION_FUNCIONAL VARCHAR2(200)
	);
	
	CREATE TABLE ROLES_MEDAS (
		ID NUMBER(38 , 0) NOT NULL,
		USUARIO VARCHAR2(100),
		ROLE VARCHAR2(100)
	);
	
	CREATE TABLE USUARIOS_APLICACIONES (
		ID_USUARIO VARCHAR2(30) NOT NULL,
		ID_APLICATIVO NUMBER(38 , 0) NOT NULL
